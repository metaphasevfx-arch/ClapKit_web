Object.assign(i18n.ru, i18nExtra.ru);
Object.assign(i18n.en, i18nExtra.en);

const STORAGE_KEY = "clapkit-site-lang";
const LANG_PARAM = "lang";

const isSupportedLang = (value) => value === "ru" || value === "en";

const getInitialLanguage = () => {
  const params = new URLSearchParams(window.location.search);
  const fromUrl = params.get(LANG_PARAM);
  if (isSupportedLang(fromUrl)) return fromUrl;

  const saved = localStorage.getItem(STORAGE_KEY);
  if (isSupportedLang(saved)) return saved;

  return "en";
};

const updateInternalLinks = (lang) => {
  const links = document.querySelectorAll("a[data-lang-link]");

  links.forEach((link) => {
    const href = link.getAttribute("href");
    if (!href || href.startsWith("#") || href.startsWith("mailto:") || href.startsWith("tel:")) {
      return;
    }

    let url;
    try {
      url = new URL(href, window.location.href);
    } catch {
      return;
    }

    if (url.origin !== window.location.origin) return;

    if (lang === "en") {
      url.searchParams.delete(LANG_PARAM);
    } else {
      url.searchParams.set(LANG_PARAM, lang);
    }

    link.setAttribute("href", `${url.pathname}${url.search}${url.hash}`);
  });
};

const updateUrlState = (lang) => {
  const url = new URL(window.location.href);
  if (lang === "en") {
    url.searchParams.delete(LANG_PARAM);
  } else {
    url.searchParams.set(LANG_PARAM, lang);
  }
  window.history.replaceState({}, "", `${url.pathname}${url.search}${url.hash}`);
};

const applyLanguage = (lang) => {
  const locale = isSupportedLang(lang) ? lang : "en";
  const dict = i18n[locale];

  document.documentElement.lang = locale;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-content]").forEach((el) => {
    const key = el.getAttribute("data-i18n-content");
    if (dict[key]) {
      el.setAttribute("content", dict[key]);
    }
  });

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === locale);
  });

  const heroImage = document.querySelector("[data-hero-image]");
  if (heroImage) {
    const ruSrc = heroImage.getAttribute("data-src-ru");
    const enSrc = heroImage.getAttribute("data-src-en");
    heroImage.src = locale === "ru" ? ruSrc : enSrc;
  }

  localStorage.setItem(STORAGE_KEY, locale);
  updateInternalLinks(locale);
  updateUrlState(locale);
};

const initLanguage = () => {
  const initial = getInitialLanguage();
  applyLanguage(initial);

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.addEventListener("click", () => applyLanguage(button.dataset.lang));
  });
};

const initFooterYear = () => {
  const yearNode = document.getElementById("year");
  if (yearNode) {
    yearNode.textContent = new Date().getFullYear();
  }
};

initLanguage();
initFooterYear();
