function applyLanguage(lang) {
  const locale = i18n[lang] ? lang : "en";
  document.documentElement.lang = locale;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (i18n[locale][key]) {
      el.textContent = i18n[locale][key];
    }
  });

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === locale);
  });

  localStorage.setItem("clapkit-site-lang", locale);
}

function setupLanguage() {
  const saved = localStorage.getItem("clapkit-site-lang");
  const browserPref = navigator.language && navigator.language.toLowerCase().startsWith("ru") ? "ru" : "en";
  const initial = saved || browserPref;
  applyLanguage(initial);

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.addEventListener("click", () => applyLanguage(button.dataset.lang));
  });
}

function setYear() {
  const node = document.getElementById("year");
  if (node) node.textContent = new Date().getFullYear();
}

setupLanguage();
setYear();
